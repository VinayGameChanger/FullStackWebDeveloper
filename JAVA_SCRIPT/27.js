//properties of an array 

//push
let fruits=["apple","banana","kela"];
console.log(fruits);

fruits.push("mango");//push-add items in last
console.log(fruits);

fruits.pop(); //pop-remove elements from last-it also return an item which it removed
console.log(fruits);

//unshift-add element at start
fruits.unshift("Grapes");
console.log(fruits);

//shift-remove element from the start-it also return an item which it removed
fruits.shift();
console.log(fruits);
